package com.ajay.entity;

public class Royalty {

	private String royaltyPoints;
	private String customerId;
	private String royaltyId;
	private String departmentNumber;
	private String bookId;
	private String agentCommession;

	public String getRoyaltyPoints() {
		return royaltyPoints;
	}

	public void setRoyaltyPoints(String royaltyPoints) {
		this.royaltyPoints = royaltyPoints;
	}

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getRoyaltyId() {
		return royaltyId;
	}

	public void setRoyaltyId(String royaltyId) {
		this.royaltyId = royaltyId;
	}

	public String getDepartmentNumber() {
		return departmentNumber;
	}

	public void setDepartmentNumber(String departmentNumber) {
		this.departmentNumber = departmentNumber;
	}

	public String getBookId() {
		return bookId;
	}

	public void setBookId(String bookId) {
		this.bookId = bookId;
	}

	public String getAgentCommession() {
		return agentCommession;
	}

	public void setAgentCommession(String agentCommession) {
		this.agentCommession = agentCommession;
	}
	
	
}
